a = 14
b = 3

#operasi aritmatika (+)
hasil = a + b
print(a, '+' ,b, '=' ,hasil)

#operasi aritmatika (-)
hasil = a - b
print(a, '-' ,b, '=' ,hasil)

#operasi aritmatika (*)
hasil = a * b
print(a, '*' ,b, '=' ,hasil)

#operasi aritmatika (/)
hasil = a / b
print(a, '/' ,b, '=' ,hasil)

#operasi aritmatika (pangkat)
hasil = a ** b
print(a, '**' ,b, '=' ,hasil)

#operasi aritmatika (modulus)
hasil = a % b
print(a, '%' ,b, '=' ,hasil)

#operasi aritmatika (floor devision)
hasil = a // b
print(a, '//' ,b, '=' ,hasil)

